import fs from "node:fs/promises"
import path from "node:path"
import { fileURLToPath } from "node:url"

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, "../..")
const tooltipSourcePath = path.join(repoRoot, "TOOLTIPS.md")
const outputPath = path.join(repoRoot, "webapp/lib/generated-tooltip-content.ts")

function parseHeading(line, level) {
  const match = line.match(new RegExp(`^${"#".repeat(level)}\\s+(.+?)\\s+\\{#([A-Za-z0-9_-]+)\\}\\s*$`))
  if (!match) return null
  return {
    title: match[1].trim(),
    id: match[2],
  }
}

function flushParagraph(buffer, blocks) {
  if (buffer.length === 0) return
  blocks.push({
    type: "paragraph",
    text: buffer.join(" ").trim(),
  })
  buffer.length = 0
}

function flushList(items, blocks) {
  if (items.length === 0) return
  blocks.push({
    type: "list",
    items: [...items],
  })
  items.length = 0
}

async function main() {
  const source = await fs.readFile(tooltipSourcePath, "utf8")
  const lines = source.replace(/\r\n/g, "\n").split("\n")

  /** @type {{ title: string; topics: { id: string; title: string; blocks: any[] }[] }[]} */
  const groups = []
  let currentGroup = null
  let currentTopic = null
  let paragraphBuffer = []
  let listBuffer = []

  const finalizeTopic = () => {
    if (!currentTopic) return
    flushParagraph(paragraphBuffer, currentTopic.blocks)
    flushList(listBuffer, currentTopic.blocks)
    currentGroup?.topics.push(currentTopic)
    currentTopic = null
  }

  for (const rawLine of lines) {
    const line = rawLine.replace(/\s+$/, "")
    if (line.startsWith("# ")) continue

    if (line.startsWith("## ")) {
      finalizeTopic()
      currentGroup = {
        title: line.slice(3).trim(),
        topics: [],
      }
      groups.push(currentGroup)
      continue
    }

    const topicHeading = parseHeading(line, 3)
    if (topicHeading) {
      finalizeTopic()
      if (!currentGroup) {
        throw new Error(`Tooltip "${topicHeading.id}" appears before any group heading`)
      }
      currentTopic = {
        ...topicHeading,
        blocks: [],
      }
      continue
    }

    const listMatch = line.match(/^- (.+)$/)
    if (listMatch) {
      if (!currentTopic) continue
      flushParagraph(paragraphBuffer, currentTopic.blocks)
      listBuffer.push(listMatch[1].trim())
      continue
    }

    if (/^\s{2,}\S/.test(rawLine) && listBuffer.length > 0) {
      listBuffer[listBuffer.length - 1] = `${listBuffer[listBuffer.length - 1]} ${rawLine.trim()}`
      continue
    }

    if (line.trim() === "") {
      if (currentTopic) {
        flushParagraph(paragraphBuffer, currentTopic.blocks)
        flushList(listBuffer, currentTopic.blocks)
      }
      continue
    }

    if (!currentTopic) continue
    flushList(listBuffer, currentTopic.blocks)
    paragraphBuffer.push(line.trim())
  }

  finalizeTopic()

  const seenIds = new Set()
  const flattenedTopics = []

  for (const group of groups) {
    for (const topic of group.topics) {
      if (seenIds.has(topic.id)) throw new Error(`Duplicate tooltip id "${topic.id}"`)
      seenIds.add(topic.id)
      flattenedTopics.push({
        id: topic.id,
        groupTitle: group.title,
        title: topic.title,
        blocks: topic.blocks,
      })
    }
  }

  const output = `/* eslint-disable */
// This file is generated by webapp/scripts/generate-tooltip-content.mjs from TOOLTIPS.md.
// Do not edit this file directly.

export type TooltipBlock =
  | { type: "paragraph"; text: string }
  | { type: "list"; items: string[] }

export type TooltipTopic = {
  id: string
  title: string
  blocks: TooltipBlock[]
}

export type TooltipGroup = {
  title: string
  topics: TooltipTopic[]
}

export type LayoutOpenTooltipItem = {
  id: string
  groupTitle: string
  title: string
  blocks: TooltipBlock[]
}

export const TOOLTIP_CONTENT_GROUPS = ${JSON.stringify(groups, null, 2)} as const satisfies readonly TooltipGroup[]

export const LAYOUT_OPEN_TOOLTIP_ITEMS = ${JSON.stringify(flattenedTopics, null, 2)} as const satisfies readonly LayoutOpenTooltipItem[]
`

  await fs.writeFile(outputPath, output)
  process.stdout.write(`Generated tooltip content from ${path.relative(repoRoot, tooltipSourcePath)}\n`)
}

main().catch((error) => {
  console.error(error)
  process.exitCode = 1
})
