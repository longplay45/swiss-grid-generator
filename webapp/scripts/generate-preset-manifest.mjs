import fs from "node:fs/promises"
import path from "node:path"
import { fileURLToPath } from "node:url"

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..")
const PRESET_DATA_DIR = path.join(ROOT, "lib", "presets", "data")
const OUTPUT_PATH = path.join(ROOT, "lib", "presets", "generated-manifest.ts")

function escapeTsString(value) {
  return value.replaceAll("\\", "\\\\").replaceAll("\"", "\\\"")
}

function isObjectRecord(value) {
  return typeof value === "object" && value !== null
}

function getPresetValidationError(payload) {
  if (!isObjectRecord(payload)) return "expected a top-level object"
  if (!Array.isArray(payload.pages)) return "missing canonical pages[]"
  if (payload.pages.length === 0) return "pages[] must include at least one page"
  if (typeof payload.activePageId !== "string" || payload.activePageId.trim().length === 0) {
    return "missing activePageId"
  }
  if (!payload.pages.some((page) => isObjectRecord(page) && page.id === payload.activePageId)) {
    return "activePageId does not reference an existing page"
  }
  return null
}

function indentBlock(value, prefix) {
  return value
    .split("\n")
    .map((line) => `${prefix}${line}`)
    .join("\n")
}

function buildManifestSource(entries) {
  const manifestEntries = entries
    .map(({ fileName, payload }) => [
      "  {",
      `    path: "./data/${escapeTsString(fileName)}",`,
      `${indentBlock(JSON.stringify(payload, null, 2), "    ").replace(/^    \{/, "    source: {")},`,
      "  },",
    ].join("\n"))
    .join("\n")

  return [
    "// This file is auto-generated by scripts/generate-preset-manifest.mjs.",
    "// Do not edit it manually.",
    "",
    "export const GENERATED_PRESET_MANIFEST = [",
    manifestEntries,
    "] as const",
    "",
  ].join("\n")
}

async function main() {
  const dirEntries = await fs.readdir(PRESET_DATA_DIR, { withFileTypes: true })
  const fileNames = dirEntries
    .filter((entry) => entry.isFile() && entry.name.endsWith(".json"))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b))

  if (fileNames.length === 0) {
    throw new Error(`No preset JSON files found in ${PRESET_DATA_DIR}`)
  }

  const validEntries = []
  let skippedCount = 0

  for (const fileName of fileNames) {
    const filePath = path.join(PRESET_DATA_DIR, fileName)
    let payload
    try {
      payload = JSON.parse(await fs.readFile(filePath, "utf8"))
    } catch (error) {
      skippedCount += 1
      console.warn(
        `Skipping preset "${fileName}": ${error instanceof Error ? error.message : String(error)}`,
      )
      continue
    }

    const validationError = getPresetValidationError(payload)
    if (validationError) {
      skippedCount += 1
      console.warn(`Skipping preset "${fileName}": ${validationError}`)
      continue
    }

    validEntries.push({ fileName, payload })
  }

  if (validEntries.length === 0) {
    throw new Error(`No valid preset JSON files found in ${PRESET_DATA_DIR}`)
  }

  const nextSource = buildManifestSource(validEntries)
  const currentSource = await fs.readFile(OUTPUT_PATH, "utf8").catch(() => "")

  if (currentSource === nextSource) {
    console.log(`Preset manifest already up to date (${validEntries.length} files, ${skippedCount} skipped).`)
    return
  }

  await fs.writeFile(OUTPUT_PATH, nextSource, "utf8")
  console.log(`Generated preset manifest (${validEntries.length} files, ${skippedCount} skipped).`)
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error))
  process.exitCode = 1
})
